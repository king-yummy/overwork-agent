"""Stage 1 검증 — 정답을 손으로 계산한 합성 데이터로 대조.

원본 개인정보 없이, 53개 열을 가진 가짜 정보검색 표를 만들어
필터 3종과 BN(조정계) 계산이 정확한지 assert로 확인한다.
"""
import pandas as pd
from stage1_chogeun import build_chogeun_master

NCOL = 53  # 정보검색 A~BA

def row(**kw):
    """위치 인덱스로 53칸짜리 행 생성. 안 채운 칸은 빈 문자열."""
    r = [""] * NCOL
    idx = dict(구분자=0, 근무일자=1, 사번=2, 성명=3, 업무내용=9, 근무구분=14,
               야간=16, 휴일=17, 평일연장=18, 휴일연장=19,
               휴식초과=44, 휴식근무=46, 제외식사=48, 제외9이상=50)
    for k, v in kw.items():
        r[idx[k]] = v
    return r

rows = [
    # R1 정상: BN = max(2,0,3,0) - 30/60 - 0 - 0 - 0 = 2.5
    row(구분자=1, 사번="0439", 성명="A", 업무내용="OLT작업", 근무구분="초근",
        야간=2, 평일연장=3, 휴식초과=30),
    # R2 비대상→초근: BN = max(1.5,..) - 0 - 60/60 - 0 = 0.5
    row(구분자=1, 사번="1018", 성명="B", 업무내용="점검", 근무구분="비대상",
        야간=1.5, 휴식근무=60),
    # R3 교대근무 → 삭제
    row(구분자=1, 사번="2000", 성명="C", 업무내용="교대근무", 근무구분="초근",
        야간=9),
    # R4 사번 P → 삭제
    row(구분자=1, 사번="P123", 성명="D", 업무내용="x", 근무구분="초근", 야간=9),
    # R5 사번 Y → 삭제
    row(구분자=1, 사번="Y999", 성명="E", 업무내용="x", 근무구분="초근", 야간=9),
    # R6 차감 포함: BN = max(0,5,0,0) - 0 - 0 - 1 - 0.5 = 3.5
    row(구분자=1, 사번="3000", 성명="F", 업무내용="작업", 근무구분="초근",
        휴일=5, 제외식사=1, 제외9이상=0.5),
]
jeongbo = pd.DataFrame(rows)

res = build_chogeun_master(jeongbo, geunmu_month="4월")
m, rep = res.master, res.report

print("처리 리포트:", rep)
print("\n남은 행 (사번 / 근무구분 / 조정계):")
for _, x in m.iterrows():
    print(f"  {x['사번']:6s}  {x['근무구분']:4s}  BN={x['조정계']}")

# --- 검증 ---
assert rep["입력행"] == 6
assert rep["교대근무_삭제"] == 1
assert rep["사번PY_삭제"] == 2
assert rep["비대상→초근_변환"] == 1
assert rep["출력행"] == 3
assert set(m["사번"]) == {"0439", "1018", "3000"}

bn = dict(zip(m["사번"], m["조정계"]))
assert abs(bn["0439"] - 2.5) < 1e-9, bn["0439"]
assert abs(bn["1018"] - 0.5) < 1e-9, bn["1018"]
assert abs(bn["3000"] - 3.5) < 1e-9, bn["3000"]

# 비대상 → 초근 변환 확인
assert m.loc[m["사번"] == "1018", "근무구분"].iloc[0] == "초근"
# 근무월 채워졌는지
assert (m["근무월"] == "4월").all()
# 컬럼 위치/개수 (총 66열)
assert m.shape[1] == 66

print("\n✅ 모든 검증 통과 — 필터 3종 + BN 계산 + 재배치 정확")
